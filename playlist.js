/* DOM */

const container = document.getElementById("songs-container");
const audio = document.getElementById("audioPlayer");
const volumeSlider = document.querySelector('.volume-slider');
const footerTitle = document.getElementById("footer-title");
const footerArtist = document.getElementById("footer-artist");
const footerImg = document.getElementById("footer-img");

/* Render Songs */
container.innerHTML = songs.map((song, i) => `
    <div class="song-row"
        data-index="${i}">
        <span>${i + 1}</span>
        <div class="song-title-meta">
            <img src="${song.img}" class="song-thumb">
            <div>
                <p class="song-name">${song.title}</p>
                <p class="artist-name">${song.artist}</p>
            </div>
        </div>
        <span class="col-album">${song.album}</span>
        <span class="col-time">${song.time}</span>
    </div>
`).join("");

/* Click Logic */
document.querySelectorAll(".song-row").forEach(row => {
    row.addEventListener("click", () => {
        document.querySelectorAll(".song-row")
            .forEach(r => r.classList.remove("active"));

        row.classList.add("active");

        const song = songs[row.dataset.index];
        audio.src = song.path;
        audio.play();

        footerTitle.textContent = song.title;
        footerArtist.textContent = song.artist;
        footerImg.src = song.img;


        volumeSlider.addEventListener('input', (e) => {
    // Volume is a value between 0 and 1. We divide the 0-100 slider value by 100.
    audio.volume = e.target.value / 100;
});

    });
});
